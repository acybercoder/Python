"""
Things to Remember
Virtual environments allow you to use pip to install many different versions of the same package on the same machine without conflicts.
Virtual environments are created with pyvenv, enabled with source bin/activate, and disabled with deactivate.
You can dump all of the requirements of an environment with pip freeze. You can reproduce the environment by supplying the
requirements.txt file to pip install -r.
In versions of Python before 3.4, the pyvenv tool must be downloaded and installed separately. The command-line tool is called virtualenv
instead of pyvenv.
"""